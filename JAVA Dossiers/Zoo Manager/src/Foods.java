public abstract class Foods {
    private static double meat;
    private static double plants;
    private static double fish;

    //GETTERS
    public static double getMeat() {return meat;}
    public static double getPlants() {return plants;}
    public static double getFish() {return fish;}

    //SETTERS
    public static void setMeat(double meat) {Foods.meat = meat;}
    public static void setPlants(double plants) {Foods.plants = plants;}
    public static void setFish(double fish) {Foods.fish = fish;}
}
